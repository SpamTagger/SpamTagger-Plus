use v5.40;
use strict;
use warnings;
use utf8;
#use Carp qw( confess );

my ($SRCDIR);
BEGIN {
    if ($0 =~ m/(\S*)\/\S+.pl$/) {
        my $path = $1."/../lib";
        unshift (@INC, $path);
    }
    require ReadConfig;
    my $conf = ReadConfig::get_instance();
    $SRCDIR = $conf->get_option('SRCDIR') || '/usr/spamtagger';
    unshift(@INC, $SRCDIR."/lib");
}

use dump_domains qw( dump_domains );

dump_domains(@_);
